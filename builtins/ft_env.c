#include "../minishell.h"

int ft_env(t_env *cmd)
{
    int i;

    i = 0;
    while (cmd->my_envp[i])
    {
        if (strchr(cmd->my_envp[i], '='))
            printf("%s\n",cmd->my_envp[i]);
        i++;
    }
    return (0);
}